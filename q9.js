// the code difference between for loop, while loop, and do...while loop is:
// For Loop:
for (initialization; condition; increment/decrement) {
    // code to be executed
}

// While Loop:
while (condition) {
    // code to be executed
}

// Do...While Loop:
do {
    // code to be executed
} while (condition);
  